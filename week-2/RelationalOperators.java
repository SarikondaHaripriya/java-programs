class RelationalOperators
  {
    public static void main(String args[])
    {
      int a=10,b=60;
      System.out.println(26>56);
      System.out.println(a<b);
      System.out.println(a<=b);
      System.out.println(a>=b);
      System.out.println(a==b);
      System.out.println(a!=b);
        
    }
  }